public void AddMaterias(int n, JPanel parentPanel, JPanel container, Color color) {
     container.removeAll();
     JPanel container2 = new JPanel();
     container2.setLayout(null);
     container2.setBounds(0, 22, parentPanel.getWidth(), parentPanel.getHeight());
     container2.setBackground(parentPanel.getBackground());
     parentPanel.add(container2);
     
     int circleWidth = 50;
     int circleHeight = 50;
     int maxCirclesPerRow = container2.getWidth() / circleWidth;

     int currentX = 0;
     int currentY = 10;
     int totalWidth = 0;

     for (int i = 0; i < n; i++) {
         CirclePanel circlePanel = new CirclePanel(15,50);
         circlePanel.setBounds(currentX, currentY, circleWidth, circleHeight);
         circlePanel.setColor(color);
         container2.add(circlePanel);
         
         currentX += circleWidth;
         totalWidth += circleWidth;

         if (totalWidth >= container2.getWidth() || i % maxCirclesPerRow == maxCirclesPerRow - 1) {
             currentX = 0;
             currentY += circleHeight;
             totalWidth = 0;
         }
     }
     container2.repaint();
     System.out.println("agregado");
}





    
    
    
    
    
