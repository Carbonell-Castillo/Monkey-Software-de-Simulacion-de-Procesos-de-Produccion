package Data;

/**
 *
 * @author bruce
 */
public class MateriaPrima {
    private int id, location;
    
    public MateriaPrima(){
        id=0;
        location=0;
    }
    
    public MateriaPrima(int id, int location) {
        this.id = id;
        this.location = location;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLocation() {
        return location;
    }

    public void setLocation(int location) {
        this.location = location;
    }
    
    
    
}
